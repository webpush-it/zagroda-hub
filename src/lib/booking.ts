export interface BookingEmailContext {
  /** Site origin, e.g. `https://zagroda.example` — used to build the cancel link. */
  origin: string;
  /** Request id generated by the route (uuid) — builds the owner deep link. */
  requestId: string;
  /** Plaintext cancel token generated by the route (uuid). */
  cancelToken: string;
  zagrodaName: string;
  /** Owner recipient address; null when it could not be resolved (email skipped). */
  ownerEmail: string | null;
  /** Human-readable turnus label, when known. */
  turnusLabel: string | null;
  guest_name: string;
  guest_email: string;
  guest_phone: string;
  trip_date: string;
  participants_count: number;
  /** Group type chosen by the guest; null for legacy/untyped rows. */
  group_type: GroupType | null;
}

/**
 * Pure builder for the two transactional emails a submission triggers. Kept
 * pure (no IO) so it is unit-testable and so every guest-supplied field is
 * provably escaped before it lands in HTML. Returns `owner: null` when no
 * owner address was resolved.
 */
export function buildBookingEmails(ctx: BookingEmailContext): { guest: EmailMessage; owner: EmailMessage | null } {
  const name = escapeHtml(ctx.zagrodaName);
  const guestName = escapeHtml(ctx.guest_name);
  const guestEmail = escapeHtml(ctx.guest_email);
  const guestPhone = escapeHtml(ctx.guest_phone);
  const tripDate = escapeHtml(ctx.trip_date);
  const turnus = ctx.turnusLabel ? escapeHtml(ctx.turnusLabel) : null;
  const participants = ctx.participants_count;
  const cancelUrl = `${ctx.origin}/anuluj?token=${ctx.cancelToken}`;
  const cancelUrlHtml = escapeHtml(cancelUrl);

  const summaryRows = [
    `<li>Termin: <strong>${tripDate}</strong></li>`,
    turnus ? `<li>Turnus: <strong>${turnus}</strong></li>` : null,
    `<li>Liczba uczestników: <strong>${participants}</strong></li>`,
  ]
    .filter(Boolean)
    .join("");

  const guest: EmailMessage = {
    to: ctx.guest_email,
    subject: `Potwierdzenie zapytania — ${ctx.zagrodaName}`,
    html: renderEmailLayout({
      title: "Dziękujemy za zapytanie",
      bodyHtml: `<p>Dziękujemy za przesłanie zapytania o pobyt w zagrodzie <strong>${name}</strong>. Gospodarz skontaktuje się z Tobą wkrótce.</p>
<ul>${summaryRows}</ul>
<p>Jeśli chcesz anulować to zapytanie, kliknij poniższy link:</p>
<p><a href="${cancelUrlHtml}">${cancelUrlHtml}</a></p>`,
    }),
  };

  if (!ctx.ownerEmail) {
    return { guest, owner: null };
  }

  const requestUrl = `${ctx.origin}/dashboard/zapytania/${ctx.requestId}`;
  const requestUrlHtml = escapeHtml(requestUrl);

  // Owner-only: the request summary carries the group type. The guest email
  // (summaryRows above) is unchanged — the type surfaces only owner-side.
  const groupTypeLabel = ctx.group_type ? GROUP_TYPE_LABELS[ctx.group_type] : "—";
  const ownerSummaryRows = `${summaryRows}<li>Typ grupy: <strong>${escapeHtml(groupTypeLabel)}</strong></li>`;

  const owner: EmailMessage = {
    to: ctx.ownerEmail,
    replyTo: ctx.guest_email,
    subject: `Masz nowe zapytanie — ${ctx.zagrodaName}`,
    html: renderEmailLayout({
      title: "Nowe zapytanie o pobyt",
      bodyHtml: `<p>Otrzymałeś nowe zapytanie o pobyt w zagrodzie <strong>${name}</strong>.</p>
<ul>${ownerSummaryRows}</ul>
<p>Dane kontaktowe osoby kontaktowej:</p>
<ul>
<li>Imię i nazwisko: <strong>${guestName}</strong></li>
<li>E-mail: <strong>${guestEmail}</strong></li>
<li>Telefon: <strong>${guestPhone}</strong></li>
</ul>
<p><a href="${requestUrlHtml}">Zobacz zapytanie</a></p>
<p>Aby odpowiedzieć, użyj opcji „Odpowiedz” — wiadomość trafi bezpośrednio do osoby kontaktowej.</p>`,
    }),
  };

  return { guest, owner };
}

export interface DecisionEmailContext {
  guest_name: string;
  guest_email: string;
  zagroda_name: string;
  trip_date: string;
  turnus_label: string | null;
  participants_count: number;
}

function decisionSummaryRows(ctx: DecisionEmailContext): string {
  const turnus = ctx.turnus_label ? escapeHtml(ctx.turnus_label) : null;
  return [
    `<li>Termin: <strong>${escapeHtml(ctx.trip_date)}</strong></li>`,
    turnus ? `<li>Turnus: <strong>${turnus}</strong></li>` : null,
    `<li>Liczba uczestników: <strong>${ctx.participants_count}</strong></li>`,
  ]
    .filter(Boolean)
    .join("");
}

/**
 * Acceptance notification to the guest (FR-005). Final-state message: no
 * reply-to and no cancel link — the decision closes the request's lifecycle.
 */
export function buildAcceptanceEmail(ctx: DecisionEmailContext): EmailMessage {
  const name = escapeHtml(ctx.zagroda_name);
  const guestName = escapeHtml(ctx.guest_name);
  return {
    to: ctx.guest_email,
    subject: `Rezerwacja potwierdzona — ${ctx.zagroda_name}`,
    html: renderEmailLayout({
      title: "Rezerwacja potwierdzona",
      bodyHtml: `<p>Dzień dobry, ${guestName}!</p>
<p>Gospodarz zagrody <strong>${name}</strong> zaakceptował Twoje zapytanie o pobyt. Rezerwacja jest potwierdzona.</p>
<ul>${decisionSummaryRows(ctx)}</ul>
<p>Do zobaczenia w zagrodzie!</p>`,
    }),
  };
}

/**
 * Rejection notification to the guest. Same final-state posture as
 * buildAcceptanceEmail: no reply-to, no cancel link.
 */
export function buildRejectionEmail(ctx: DecisionEmailContext): EmailMessage {
  const name = escapeHtml(ctx.zagroda_name);
  const guestName = escapeHtml(ctx.guest_name);
  return {
    to: ctx.guest_email,
    subject: `Zapytanie odrzucone — ${ctx.zagroda_name}`,
    html: renderEmailLayout({
      title: "Zapytanie odrzucone",
      bodyHtml: `<p>Dzień dobry, ${guestName}.</p>
<p>Niestety gospodarz zagrody <strong>${name}</strong> odrzucił Twoje zapytanie o pobyt w wybranym terminie.</p>
<ul>${decisionSummaryRows(ctx)}</ul>
<p>Zachęcamy do wyszukania innej zagrody lub innego terminu w katalogu Zagroda Hub.</p>`,
    }),
  };
}

/**
 * Withdrawal notification to the guest (FR-016): the owner undid a previous
 * acceptance. Same final-state posture as the other decision emails: no
 * reply-to, no links.
 */
export function buildWithdrawalEmail(ctx: DecisionEmailContext): EmailMessage {
  const name = escapeHtml(ctx.zagroda_name);
  const guestName = escapeHtml(ctx.guest_name);
  return {
    to: ctx.guest_email,
    subject: `Rezerwacja wycofana — ${ctx.zagroda_name}`,
    html: renderEmailLayout({
      title: "Rezerwacja wycofana",
      bodyHtml: `<p>Dzień dobry, ${guestName}.</p>
<p>Gospodarz zagrody <strong>${name}</strong> wycofał wcześniej potwierdzoną rezerwację. Rezerwacja nie jest już aktualna.</p>
<ul>${decisionSummaryRows(ctx)}</ul>
<p>Zachęcamy do wyszukania innej zagrody lub innego terminu w katalogu Zagroda Hub.</p>`,
    }),
  };
}
