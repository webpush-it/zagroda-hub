const DATE_RE = /^\d{4}-\d{2}-\d{2}$/;

/**
 * Group-type storage tokens (ASCII, mirroring booking_source/request_status
 * style). Human labels live in the presentation layer, mapped from these.
 */
export const GROUP_TYPE_VALUES = ["szkola", "przedszkole", "grupa_indywidualna", "inna"] as const;

export type GroupType = (typeof GROUP_TYPE_VALUES)[number];

/** Human labels for the group-type storage tokens (presentation layer). */
export const GROUP_TYPE_LABELS: Record<GroupType, string> = {
  szkola: "Szkoła",
  przedszkole: "Przedszkole",
  grupa_indywidualna: "Grupa indywidualna",
  inna: "Inna",
};

/** Strip the cosmetic separators a user may type in a phone number. */
export function normalizePhone(raw: string): string {
  return raw.replace(/[\s\-()]/g, "");
}

/**
 * Lenient PL mobile/landline check: an optional `+48` / `0048` / `48` country
 * prefix followed by exactly 9 national digits. Spacing/dashes/parens are
 * stripped first via normalizePhone.
 */
export function isValidPlPhone(raw: string): boolean {
  return /^(\+48|0048|48)?\d{9}$/.test(normalizePhone(raw));
}

/** True iff `raw` is a real calendar date in strict YYYY-MM-DD form. */
function isRealDate(raw: string): boolean {
  if (!DATE_RE.test(raw)) return false;
  const parsed = new Date(`${raw}T00:00:00Z`);
  return !Number.isNaN(parsed.getTime()) && parsed.toISOString().slice(0, 10) === raw;
}

/** True iff `raw` is a real date that is today or later (server-side authority). */
export function isTodayOrFuture(raw: string): boolean {
  if (!isRealDate(raw)) return false;
  const today = new Date().toISOString().slice(0, 10);
  return raw >= today;
}

export const bookingRequestSchema = z.object({
  zagroda_id: z.uuid("Nieprawidłowy identyfikator zagrody"),
  turnus_id: z.uuid("Wybierz turnus"),
  trip_date: z
    .string("Podaj datę")
    .regex(DATE_RE, "Podaj datę w formacie RRRR-MM-DD")
    .refine(isRealDate, "Nieprawidłowa data")
    .refine(isTodayOrFuture, "Data nie może być w przeszłości"),
  participants_count: z.coerce
    .number("Podaj liczbę uczestników")
    .int("Liczba uczestników musi być liczbą całkowitą")
    .min(1, "Co najmniej 1 uczestnik")
    .max(1000, "Maksymalnie 1000 uczestników"),
  group_type: z.enum(GROUP_TYPE_VALUES, "Wybierz typ grupy"),
  guest_name: z
    .string("Podaj imię i nazwisko")
    .trim()
    .min(1, "Podaj imię i nazwisko")
    .max(120, "Maksymalnie 120 znaków"),
  guest_email: z.email("Podaj prawidłowy adres e-mail"),
  guest_phone: z
    .string("Podaj numer telefonu")
    .trim()
    .min(1, "Podaj numer telefonu")
    .refine(isValidPlPhone, "Podaj prawidłowy numer telefonu (9 cyfr, opcjonalnie z +48)"),
});

export type BookingRequestInput = z.infer<typeof bookingRequestSchema>;

